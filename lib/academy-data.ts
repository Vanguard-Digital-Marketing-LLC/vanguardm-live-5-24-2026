export type QuizQuestion =
  | {
      type: "multiple-choice";
      question: string;
      options: string[];
      correctIndex: number;
      explanation: string;
    }
  | {
      type: "true-false";
      question: string;
      correctAnswer: boolean;
      explanation: string;
    }
  | {
      type: "multi-select";
      question: string;
      options: string[];
      correctIndices: number[];
      explanation: string;
    }
  | {
      type: "ordering";
      question: string;
      items: string[];
      correctOrder: number[];
      explanation: string;
    };

